package ex1;

/**
 * Cette classe contient des méthode classique de calcul: addition et soustraction
 * @author ETY0006
 *
 */

public class Calcul {

	/**
	 * methode pour additionner deux entiers
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * méthode pour soustraire deux entiers
	 * @param a
	 * @param b
	 * @return a-b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
